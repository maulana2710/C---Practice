#include <iostream>
using namespace std;

int main()
{
    /*deklarasi variabel bertipe integer dengan nama i*/
    /*buatlah perulangan for dengan ketentuan 
    - nilai awal sama dengan 1
    - batas akhir kurang dari sama dengan 10
    - iterasinya i bertambah 1 secara postfix*/
    for (int i = 1; i <= 10; i++){
        cout <<"Saya berjanji tidak akan terlambat lagi"<< '\n';
    }
}
