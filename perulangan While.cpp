#include<iostream>
using namespace std;

int main(){

    /*inisialisasi variabel bertipe integer dengan nama variabel x bernilai 0*/
    int x = 0;

    cout<<"Berikut adalah hasil dari perkalian 3 dengan 0-10 :"<<endl;

    /*buatlah perulangan while dengan batas kurang dari sama dengan 30*/
    while (x <= 30)
    {
        cout << " " << x << endl;

        /*buatlah iterasi dimana x ditambah 3*/
        x = x + 3;
    }

}