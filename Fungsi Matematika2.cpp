#include<iostream>
#include <cmath>/*deklarasi header cmath*/
using namespace std;
int main()
{
    int nilai = 2;/*deklarasi variabel nilai yang bernilai 2 dengan tipe data int*/
    int n = 3; /*deklarasi variabel n yang bernilai 3 dengan tipe data int*/
    int total;
    total = pow(nilai,n);/*buat perhitungan pangkat menggunakan pow */
    cout << "Hasil dari 2 pangkat 3 adalah " << total << endl;					
}
